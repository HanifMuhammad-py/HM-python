print("While Loop")

# while kondisi :
#     aksi A
#     Aksi B

# contoh 1
angka = 0
print(f'angka sekarang adalah = {angka}')
while angka < 5 :
    angka += 1 
    print("true !")
print("end\n")


# contoh 2
angka = 0
print(f'angka sekarang adalah = {angka}')
while angka > -5 :
    angka -= 1 
    print("true !")
print("end\n")


# contoh 3
angka = 2 + 7
print(f'angka sekarang adalah = {angka}')
while angka > 5 :
    angka -= 1 
    print("true !")
print("end\n")
